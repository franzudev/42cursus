#include <unistd.h>

int	main(int argc, char *argv[])
{
	int i;
	int g;

	i = argc - 1;
	g = 0;
	while (i > 0)
	{
		while (argv[i][g] != '\0')
			write(1, &argv[i][g++], 1);
		write(1, "\n", 1);
		g = 0;
		i--;
	}
	return (0);
}
