#include <unistd.h>
#include "ft_stock_str.h"

void				ft_putstr(char *str)
{
	while (*str)
	{
		write(1, str++, 1);
	}
}

void				ft_putchar(char num)
{
	write(1, &num, 1);
}

void				ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		ft_putnbr(147483648);
		return ;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		ft_putchar(nb + 48);
	}
}

void				ft_show_tab(struct s_stock_str *par)
{
	int			i;

	i = 0;
	while (i < 5)
	{
		ft_putstr(par[i].str);
		ft_putstr("\n");
		ft_putnbr(par[i].size);
		ft_putstr("\n");
		ft_putstr(par[i].copy);
		ft_putstr("\n");
		i++;
	}
}
